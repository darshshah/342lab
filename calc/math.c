#include "math.h"

/* This function returns the result of the addition operation of the two input
values */
int add(int x, int y)
 {
        return (x + y);
 }

/* This function returns the result of the subtraction operation of the two input
values */
int sub(int x, int y)
 {
        return (x - y);
 }

/* This function returns the result of the multiplication operation of the two input
values*/
int mul(int x, int y)
 {
        return (x * y);
 }

/*This function returns the result of the division operation of the two input values*/
int div(int x, int y)
 {
        return (x / y);
 }

/*This function returns the result of the modulo operation of the two input values*/
int mod(int x, int y)
 {
        return (x % y);
 }
